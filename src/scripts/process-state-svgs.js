#!/usr/bin/env node
/**
 * Processes state SVGs from src/data/state-svgs/:
 * - Removes XML declaration and comments
 * - Adds fill="inherit" to path elements, strips existing fill
 * - Generates stateSvgs.ts with viewBox and innerHtml for each state
 *
 * Run: pnpm run process:state-svgs
 * (Or add new SVGs to src/data/state-svgs/, then run to regenerate stateSvgs.ts)
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..", "..");
const stateSvgsDir = path.join(root, "src", "data", "state-svgs");
const statesPath = path.join(root, "src", "data", "states.json");

const states = JSON.parse(fs.readFileSync(statesPath, "utf-8"));
const STATE_CODES = states.map((s) => s.code);

function cleanSvgContent(svgText) {
  // Remove XML declaration
  let content = svgText.replace(/<\?xml[^?]*\?>\s*/i, "");
  // Remove comments
  content = content.replace(/<!--[\s\S]*?-->/g, "");
  // Strip fill attributes from paths
  content = content.replace(/\s+fill="[^"]*"/gi, "");
  // Add fill="inherit" to path elements
  content = content.replace(/<path /gi, '<path fill="inherit" ');
  return content.trim();
}

function extractSvgData(svgText) {
  const viewBoxMatch = svgText.match(/viewBox="([^"]+)"/);
  const viewBox = viewBoxMatch ? viewBoxMatch[1] : "0 0 100 100";
  const innerMatch = svgText.match(/<svg[^>]*>([\s\S]*)<\/svg>/i);
  const innerHtml = innerMatch
    ? innerMatch[1]
        .replace(/<!--[\s\S]*?-->/g, "")
        .replace(/\s+fill="[^"]*"/gi, "")
        .replace(/<path /gi, '<path fill="inherit" ')
        .trim()
    : "";
  return { viewBox, innerHtml };
}

fs.mkdirSync(stateSvgsDir, { recursive: true });

const stateSvgsEntries = [];

for (const code of STATE_CODES) {
  const srcPath = path.join(stateSvgsDir, `${code}.svg`);
  if (!fs.existsSync(srcPath)) {
    console.warn(`Missing: ${code}.svg`);
    continue;
  }

  const raw = fs.readFileSync(srcPath, "utf-8");
  const { viewBox, innerHtml } = extractSvgData(raw);

  const destPath = path.join(stateSvgsDir, `${code}.svg`);
  const cleanedFullSvg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${viewBox}">${innerHtml}</svg>`;
  fs.writeFileSync(destPath, cleanedFullSvg, "utf-8");

  stateSvgsEntries.push({ code, viewBox, innerHtml });
}

const tsContent = `/**
 * Generated by src/scripts/process-state-svgs.js
 * Do not edit manually.
 */

export const stateSvgs: Record<string, { viewBox: string; innerHtml: string }> = {
${stateSvgsEntries
  .map(
    ({ code, viewBox, innerHtml }) =>
      `  ${code}: { viewBox: ${JSON.stringify(viewBox)}, innerHtml: ${JSON.stringify(innerHtml)} },`
  )
  .join("\n")}
};

export const STATE_CODES = ${JSON.stringify(STATE_CODES)} as const;
export type StateCode = (typeof STATE_CODES)[number];
`;

const stateSvgsPath = path.join(root, "src", "components", "StateIcon", "stateSvgs.ts");
fs.writeFileSync(stateSvgsPath, tsContent, "utf-8");

console.log(`Processed ${stateSvgsEntries.length} state SVGs`);
console.log(`Wrote ${stateSvgsPath}`);
